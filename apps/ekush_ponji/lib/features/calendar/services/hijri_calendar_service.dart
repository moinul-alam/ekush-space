  /// Convert a Gregorian [DateTime] to a [HijriDate].
  ///
  /// Uses Umm al-Qura table for 1343–1500 AH (1924–2077 CE).
  /// Outside that range uses tabular arithmetic (±1–2 days).
  /// GOB Bangladesh moon-sighting offset applied from HijriOffsetSyncService.
  HijriDate getHijriDate(DateTime gDate) {
    final jdn = _gregorianToJdn(gDate.year, gDate.month, gDate.day);
    final raw = _jdnToHijri(jdn);
    final offset = HijriOffsetSyncService.getOffsetForDate(gDate);
    if (offset == 0) return raw;
    return _applyOffset(raw, offset);
  }

  /// Returns a display string for the Hijri month(s) overlapping a
  /// Gregorian month. e.g. "Sha'ban – Ramadan 1447" or "শাবান – রমজান ১৪৪৭"
  String getHijriMonthsDisplay({
    required int gregorianYear,
    required int gregorianMonth,
    required String languageCode,
  }) {
    final lastDay = DateTime(gregorianYear, gregorianMonth + 1, 0).day;
    final first = getHijriDate(DateTime(gregorianYear, gregorianMonth, 1));
    final last = getHijriDate(DateTime(gregorianYear, gregorianMonth, lastDay));

    if (first.monthNumber == last.monthNumber) {
      return '${first.monthNameForLocale(languageCode)} '
          '${first.yearForLocale(languageCode)}';
    }

    final firstName = first.monthNameForLocale(languageCode);
    final lastName = last.monthNameForLocale(languageCode);
    final yearStr = first.year == last.year
        ? last.yearForLocale(languageCode)
        : '${first.yearForLocale(languageCode)}–${last.yearForLocale(languageCode)}';
    return '$firstName – $lastName $yearStr';
  }

  // ── Core lookup ────────────────────────────────────────

  HijriDate _jdnToHijri(int jdn) {
    if (jdn >= _startJdn && jdn < _endJdn) {
      return _tableHijri(jdn);
    }
    return _tabularHijri(jdn);
  }

  /// Binary-search the _monthStartOffsets table to find which Hijri
  /// month contains [jdn], then compute the day within that month.
  HijriDate _tableHijri(int jdn) {
    final offset = jdn - _startJdn;

    int lo = 0, hi = _totalMonths - 1, found = 0;
    while (lo <= hi) {
      final mid = (lo + hi) >> 1;
      if (_monthStartOffsets[mid] <= offset) {
        found = mid;
        lo = mid + 1;
      } else {
        hi = mid - 1;
      }
    }

    final hy = _startHy + found ~/ 12;
    final hm = found % 12 + 1;
    final hd = offset - _monthStartOffsets[found] + 1;
    return HijriDate(day: hd, monthNumber: hm, year: hy);
  }

  // ── Tabular (arithmetic) fallback ──────────────────────
  //
  // Civil/tabular Islamic calendar — Type IIa (Thursday epoch).
  // Used only for dates outside the 1343–1500 AH table range.

  HijriDate _tabularHijri(int jdn) {
    final d = jdn - 1948438;
    final y = (30 * d + 10646) ~/ 10631;
    final m = (11 * (d - (10631 * y - 10617) ~/ 30) + 330) ~/ 325;
    final day =
        d - (29 * m - 29 + (6 * m - 1) ~/ 11) - (10631 * y - 10617) ~/ 30 + 1;
    return HijriDate(
      day: day.clamp(1, 30),
      monthNumber: m.clamp(1, 12),
      year: y,
    );
  }

  // ── Offset arithmetic ──────────────────────────────────

  HijriDate _applyOffset(HijriDate date, int offset) {
    if (offset == 0) return date;
    final jdn = _hijriToJdn(date);
    return _jdnToHijri(jdn + offset);
  }

  /// HijriDate → JDN (used only for offset arithmetic).
  /// Uses table for accuracy when date is in range.
  int _hijriToJdn(HijriDate h) {
    final monthIdx = (h.year - _startHy) * 12 + (h.monthNumber - 1);
    if (monthIdx >= 0 && monthIdx < _totalMonths) {
      return _startJdn + _monthStartOffsets[monthIdx] + h.day - 1;
    }
    // Tabular fallback
    return h.day +
        (29 * h.monthNumber - 29 + (6 * h.monthNumber - 1) ~/ 11) +
        (10631 * h.year - 10617) ~/ 30 +
        1948438 -
        1;
  }

  // ── Gregorian → JDN ───────────────────────────────────

  static int _gregorianToJdn(int year, int month, int day) {
    final a = (14 - month) ~/ 12;
    final y = year + 4800 - a;
    final m = month + 12 * a - 3;
    return day +
        (153 * m + 2) ~/ 5 +
        365 * y +
        y ~/ 4 -
        y ~/ 100 +
        y ~/ 400 -
        32045;
  }
}

